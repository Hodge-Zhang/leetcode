func SumSubarrayMins(arr []int) int {
	dp := make([]int, len(arr))
	sums := make([]int, len(arr))
	for i := 0; i < len(arr); i++ {
		for j := 0; j+i < len(arr); j++ {
			if i == 0 {
				dp[j] = j
			} else {
				if arr[dp[j]] > arr[j+i] {
					dp[j] = j + i
				}

			}

			sums[dp[j]]++
		}
	}
	total := 0
	for i, sum := range sums {
		total += arr[i] * sum
	}
	return total % (1e9 + 7)
}

